var Ara1 = document.getElementById("ara1");

if (localStorage.getItem("Ara1") == "tamamlandi") {
  Ara1.checked = true;
}

Ara1.onclick = function () {
  if (localStorage.getItem("Ara1") == "tamamlandi") {
    Ara1.checked = false;
    localStorage.setItem("Ara1", "tamamlanmadi");
  } else {
    localStorage.setItem("Ara1", "tamamlandi");
    Ara1.checked = true;
  }
};
